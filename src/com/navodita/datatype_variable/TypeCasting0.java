import java.util.*;

public class TypeCasting0 {
    public static void main(String[] args) {
        char ch = 'a';
        char ch2 = 'b'; // 'c' ,'d' ,'e'
        int number = ch;
        int number2 = ch2;
        System.out.println(number);
        System.out.println(number2);
    }
}
